// Declaring variables with var
var name = "Anu";
var age = 20;
console.log("Using var:");
console.log("Name:", name);
console.log("Age:", age);

// Declaring variables with let
let city = "New York";
let temperature = 75;
console.log("Using let:");
console.log("City:", city);
console.log("Temperature:", temperature);

// Declaring variables with const
const country = "Uk";
const pi = 3.14;
console.log("Using const:");
console.log("Country:", country);
console.log("Pi:", pi);

// Attempting to change variables
// var
name = "Bob"; // Allowed
console.log("Updated Name with var:", name);

// let
city = "Los Angeles"; // Allowed
console.log("Updated City with let:", city);

